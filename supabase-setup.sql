drop policy if exists "grunter grunts write" on public.grunts;
drop policy if exists "grunter comments read" on public.comments;
drop policy if exists "grunter comments write" on public.comments;
drop policy if exists "grunter reactions read" on public.reactions;
drop policy if exists "grunter reactions write" on public.reactions;
drop policy if exists "grunter battles read" on public.battles;
drop policy if exists "grunter battles write" on public.battles;
drop policy if exists "grunter battle votes read" on public.battle_votes;
drop policy if exists "grunter battle votes write" on public.battle_votes;

