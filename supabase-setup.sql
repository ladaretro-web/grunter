-- Grunter beta Supabase setup
-- Paste this whole file into Supabase SQL Editor and click Run.
--
-- This is intentionally simple for a small closed beta.
-- It lets anyone with the app link read and post Grunter data.
-- For a stricter private version later, add Supabase Auth or an invite system.

create table if not exists public.users (
  id uuid primary key,
  display_name text not null default 'Guest',
  avatar text not null default ':)',
  created_at timestamptz not null default now()
);

create table if not exists public.grunts (
  id uuid primary key,
  user_id uuid not null references public.users(id) on delete cascade,
  title text not null default 'Untitled Grunt',
  audio_path text not null,
  duration_ms integer not null default 0,
  created_at timestamptz not null default now()
);

create table if not exists public.comments (
  id bigint generated by default as identity primary key,
  grunt_id uuid not null references public.grunts(id) on delete cascade,
  user_id uuid not null references public.users(id) on delete cascade,
  body text not null check (length(body) <= 1000),
  created_at timestamptz not null default now()
);

create table if not exists public.reactions (
  id bigint generated by default as identity primary key,
  grunt_id uuid not null references public.grunts(id) on delete cascade,
  user_id uuid not null references public.users(id) on delete cascade,
  emoji text not null,
  created_at timestamptz not null default now(),
  unique (grunt_id, user_id, emoji)
);

create table if not exists public.battles (
  id bigint generated by default as identity primary key,
  grunt_a_id uuid not null references public.grunts(id) on delete cascade,
  grunt_b_id uuid not null references public.grunts(id) on delete cascade,
  status text not null default 'live' check (status in ('live', 'closed')),
  ends_at timestamptz not null default (now() + interval '24 hours'),
  winner_grunt_id uuid references public.grunts(id) on delete set null,
  created_at timestamptz not null default now()
);

create table if not exists public.battle_votes (
  id bigint generated by default as identity primary key,
  battle_id bigint not null references public.battles(id) on delete cascade,
  user_id uuid not null references public.users(id) on delete cascade,
  voted_grunt_id uuid not null references public.grunts(id) on delete cascade,
  created_at timestamptz not null default now(),
  unique (battle_id, user_id)
);

create index if not exists grunts_created_at_idx on public.grunts(created_at desc);
create index if not exists comments_grunt_id_created_at_idx on public.comments(grunt_id, created_at);
create index if not exists reactions_grunt_id_idx on public.reactions(grunt_id);
create index if not exists battles_created_at_idx on public.battles(created_at desc);
create index if not exists battle_votes_battle_id_idx on public.battle_votes(battle_id);

alter table public.users enable row level security;
alter table public.grunts enable row level security;
alter table public.comments enable row level security;
alter table public.reactions enable row level security;
alter table public.battles enable row level security;
alter table public.battle_votes enable row level security;

