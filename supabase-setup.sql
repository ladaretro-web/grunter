for select to anon using (true);

create policy "grunter users write" on public.users
for all to anon using (true) with check (true);

create policy "grunter grunts read" on public.grunts
for select to anon using (true);

create policy "grunter grunts write" on public.grunts
for insert to anon with check (true);

create policy "grunter comments read" on public.comments
for select to anon using (true);

create policy "grunter comments write" on public.comments
for insert to anon with check (true);

create policy "grunter reactions read" on public.reactions
for select to anon using (true);

create policy "grunter reactions write" on public.reactions
for insert to anon with check (true);

create policy "grunter battles read" on public.battles
for select to anon using (true);

create policy "grunter battles write" on public.battles
for all to anon using (true) with check (true);

create policy "grunter battle votes read" on public.battle_votes
for select to anon using (true);

create policy "grunter battle votes write" on public.battle_votes
for insert to anon with check (true);

insert into storage.buckets (id, name, public, file_size_limit, allowed_mime_types)
values (
  'grunts',
  'grunts',
  true,
  10485760,
  array['audio/webm', 'audio/mp4', 'audio/wav', 'audio/mpeg', 'audio/ogg']
)
on conflict (id) do update set
  public = excluded.public,
  file_size_limit = excluded.file_size_limit,
  allowed_mime_types = excluded.allowed_mime_types;

drop policy if exists "grunter audio read" on storage.objects;
drop policy if exists "grunter audio upload" on storage.objects;
drop policy if exists "grunter audio update" on storage.objects;

create policy "grunter audio read" on storage.objects
for select to anon using (bucket_id = 'grunts');

create policy "grunter audio upload" on storage.objects
for insert to anon with check (bucket_id = 'grunts');

create policy "grunter audio update" on storage.objects
for update to anon using (bucket_id = 'grunts') with check (bucket_id = 'grunts');

alter table public.grunts replica identity full;
alter table public.comments replica identity full;
alter table public.reactions replica identity full;
alter table public.battles replica identity full;
alter table public.battle_votes replica identity full;

do $$
declare
  rt_table text;
begin
  foreach rt_table in array array['grunts', 'comments', 'reactions', 'battles', 'battle_votes']
  loop
    if not exists (
      select 1
      from pg_publication_tables
      where pubname = 'supabase_realtime'
        and schemaname = 'public'
        and tablename = rt_table
    ) then
      execute format('alter publication supabase_realtime add table public.%I', rt_table);
    end if;
  end loop;
end $$;
